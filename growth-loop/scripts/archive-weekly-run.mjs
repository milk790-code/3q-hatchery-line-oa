async function main() {
  const generatedAt = new Date();
  const scores = await readJson(SCORES_PATH);
  const weekStart = scores.week?.start ?? "unknown-week";
  const weekEnd = scores.week?.end ?? "unknown-week";
  const archiveDir = path.join(ROOT, "archive", weekStart, stamp(generatedAt));
  await mkdir(archiveDir, { recursive: true });

  const files = [];
  const missingFiles = [];

  for (const source of ARTIFACTS) {
    const sourcePath = path.join(ROOT, source);
    const archivedName = safeArchiveName(source);
    const archivePath = path.join(archiveDir, archivedName);
    try {
      const content = await readFile(sourcePath);
      const sourceStat = await stat(sourcePath);
      await copyFile(sourcePath, archivePath);
      files.push({
        source,
        archived_name: archivedName,
        archive_path: archivePath,
        copied: true,
        bytes: sourceStat.size,
        sha256: createHash("sha256").update(content).digest("hex"),
      });
    } catch (error) {
      if (error.code === "ENOENT") {
        missingFiles.push(source);
        continue;
      }
      throw error;
    }
  }

  const manifest = {
    ok: missingFiles.length === 0,
    generated_at: generatedAt.toISOString(),
    week_start: weekStart,
    week_end: weekEnd,
    archive_dir: archiveDir,
    immutable_snapshot: true,
    external_effect: false,
    production_deploy_performed: false,
    public_link_change_performed: false,
    github_push_or_pr_performed: false,
    formal_post_performed: false,
    line_push_performed: false,
    customer_data_mutation_performed: false,
    payment_action_performed: false,
    delete_action_performed: false,
    expected_files: ARTIFACTS.length,
    files_archived: files.length,
    missing_files: missingFiles,
    files,
  };

  const manifestPath = path.join(archiveDir, "manifest.json");
  await writeJson(manifestPath, manifest);

  const status = {
    ok: manifest.ok,
    generated_at: manifest.generated_at,
    week_start: weekStart,
    week_end: weekEnd,
    archive_dir: archiveDir,
    manifest_path: manifestPath,
    files_archived: files.length,
    expected_files: ARTIFACTS.length,
    missing_files: missingFiles,
    immutable_snapshot: true,
    external_effect: false,
    production_deploy_performed: false,
    public_link_change_performed: false,
    github_push_or_pr_performed: false,
    formal_post_performed: false,
    line_push_performed: false,
    customer_data_mutation_performed: false,
    payment_action_performed: false,
    delete_action_performed: false,
  };
  await writeJson(STATUS_PATH, status);
  console.log(JSON.stringify(status, null, 2));
}

async function readJson(filePath) {
  const raw = await readFile(filePath, "utf8");
  return JSON.parse(raw);
}

async function writeJson(filePath, value) {
  await mkdir(path.dirname(filePath), { recursive: true });
  await writeFile(filePath, `${JSON.stringify(value, null, 2)}\n`);
}

function safeArchiveName(source) {
  return source.replaceAll("/", "__");
}

function stamp(date) {
  return date.toISOString().replace(/[-:.]/g, "").replace("Z", "Z");
}

main();
