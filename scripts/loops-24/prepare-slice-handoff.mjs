#!/usr/bin/env node
import { promises as fs } from 'node:fs';
import path from 'node:path';
import os from 'node:os';
import process from 'node:process';
import { spawnSync } from 'node:child_process';
import { createHash } from 'node:crypto';

const automationId = process.env.LOOPS_AUTOMATION_ID || 'loops-24';
const repoRoot = path.resolve(process.env.LOOPS_REPO_ROOT || process.cwd());
const codexHome = process.env.CODEX_HOME
  || (process.env.USERPROFILE ? path.join(process.env.USERPROFILE, '.codex') : path.join(os.homedir(), '.codex'));
const stateDir = path.resolve(process.env.LOOPS_STATE_DIR || path.join(codexHome, 'automations', automationId));
const handoffDir = path.join(stateDir, 'slice-handoffs');
const groupId = readArg('--group') || process.env.LOOPS_SLICE_GROUP || 'loops_control_plane';

const boundary = await readJson(path.join(stateDir, 'commit-boundaries', 'latest.json'), null);
if (!boundary) {
  throw new Error('No commit boundary plan found. Run scripts/loops-24/plan-commit-boundaries.mjs first.');
}

const statusLines = runGit(['status', '--short']).split(/\r?\n/).filter(Boolean);
const statusFingerprint = hash(statusLines.join('\n'));
if (boundary.statusFingerprint !== statusFingerprint) {
  throw new Error(`Commit boundary plan is stale. Expected ${statusFingerprint}, got ${boundary.statusFingerprint}. Re-run plan-commit-boundaries first.`);
}

const group = (boundary.groups || []).find(item => item.id === groupId);
if (!group) {
  throw new Error(`Group not found in boundary plan: ${groupId}`);
}

const records = statusLines.map(parseStatusLine);
const statusByPath = new Map(records.map(record => [record.path, record]));
const paths = (group.paths || []).slice().sort();
const now = new Date();
const stamp = now.toISOString().replace(/[-:]/g, '').replace(/\.\d{3}Z$/, 'Z');
const safeGroupId = groupId.replace(/[^a-z0-9_-]+/gi, '-');
const reportPath = path.join(handoffDir, `${stamp}-${safeGroupId}-handoff.md`);
const jsonPath = path.join(handoffDir, `${stamp}-${safeGroupId}-handoff.json`);
const latestPath = path.join(handoffDir, 'latest.json');
const latestGroupPath = path.join(handoffDir, `${safeGroupId}-latest.json`);
const stageScriptPath = path.join(handoffDir, `${stamp}-${safeGroupId}-stage.ps1`);

const payload = {
  generatedAt: now.toISOString(),
  repoRoot,
  statusFingerprint,
  group: {
    id: group.id,
    title: group.title,
    gate: group.gate,
    recommendation: group.recommendation,
    counts: group.counts,
  },
  boundaryReportPath: boundary.reportPath,
  reportPath,
  jsonPath,
  stageScriptPath,
  paths: paths.map(item => ({
    path: item,
    status: statusByPath.get(item)?.status || '??',
  })),
  commitMessage: commitMessageFor(group),
  prDraft: prDraftFor(group, paths),
  verification: verificationFor(group),
};

await fs.mkdir(handoffDir, { recursive: true });
await fs.writeFile(stageScriptPath, renderStageScript(payload), 'utf8');
await fs.writeFile(jsonPath, `${JSON.stringify(payload, null, 2)}\n`, 'utf8');
await fs.writeFile(latestPath, `${JSON.stringify(payload, null, 2)}\n`, 'utf8');
await fs.writeFile(latestGroupPath, `${JSON.stringify(payload, null, 2)}\n`, 'utf8');
await fs.writeFile(reportPath, `${renderMarkdown(payload)}\n`, 'utf8');

console.log(JSON.stringify({
  ok: true,
  group: group.id,
  gate: group.gate,
  pathCount: paths.length,
  reportPath,
  jsonPath,
  stageScriptPath,
  statusFingerprint,
}, null, 2));

function renderStageScript(payload) {
  const lines = [
    'param(',
    `  [string]$RepoRoot = '${escapePowerShellSingleQuoted(repoRoot)}'`,
    ')',
    '',
    "$ErrorActionPreference = 'Stop'",
    '',
    '# Generated by prepare-slice-handoff.mjs.',
    '# Review this file before running. It only stages the selected slice.',
    '',
    'Push-Location $RepoRoot',
    'try {',
    '  $paths = @(',
  ];

  for (const item of payload.paths) {
    lines.push(`    '${escapePowerShellSingleQuoted(item.path)}'`);
  }

  lines.push('  )');
  lines.push('  git add -- $paths');
  lines.push('  git status --short');
  lines.push('}');
  lines.push('finally {');
  lines.push('  Pop-Location');
  lines.push('}');
  return `${lines.join('\n')}\n`;
}

function renderMarkdown(payload) {
  const lines = [
    '# LOOPS Slice Handoff',
    '',
    `- generated_at: ${payload.generatedAt}`,
    `- repo: ${payload.repoRoot}`,
    `- status_fingerprint: ${payload.statusFingerprint}`,
    `- group: ${payload.group.id}`,
    `- gate: ${payload.group.gate}`,
    `- boundary_report: ${payload.boundaryReportPath}`,
    `- stage_script: ${payload.stageScriptPath}`,
    '',
    '## Recommendation',
    '',
    payload.group.recommendation,
    '',
    '## Stage Command',
    '',
    'Review first; this command is intentionally not executed by LOOPS.',
    '',
    '```powershell',
    `powershell -ExecutionPolicy Bypass -File "${payload.stageScriptPath}"`,
    '```',
    '',
    '## Commit Message',
    '',
    '```text',
    payload.commitMessage,
    '```',
    '',
    '## PR Draft',
    '',
    '```markdown',
    payload.prDraft,
    '```',
    '',
    '## Verification Commands',
    '',
    ...payload.verification.map(command => `- \`${command}\``),
    '',
    '## Paths',
    '',
  ];

  for (const item of payload.paths) {
    lines.push(`- \`${item.status} ${item.path}\``);
  }

  lines.push('');
  lines.push('## Hard Stops');
  lines.push('');
  lines.push('- This handoff does not push, deploy, merge, or publish.');
  lines.push('- Do not include Worker deploy slices in this commit unless the gate says local-review.');
  lines.push('- Do not add API keys, tokens, or secrets to any staged file.');
  return lines.join('\n');
}

function commitMessageFor(group) {
  if (group.id === 'loops_control_plane') {
    return [
      'Add LOOPS 24 control plane',
      '',
      '- add hourly runner, scheduler helpers, and local reports',
      '- add outreach, prospecting, queue reconciliation, cache audit, and boundary planning tools',
      '- document local automation usage and review gates',
    ].join('\n');
  }

  return [
    `Prepare ${group.id} slice`,
    '',
    `- group: ${group.title}`,
    `- gate: ${group.gate}`,
  ].join('\n');
}

function prDraftFor(group, paths) {
  const summary = group.id === 'loops_control_plane'
    ? [
        '- Adds the local LOOPS 24 control plane and hourly runner scripts.',
        '- Adds safe local helpers for outreach drafts, Google prospecting preview, Wrangler cache audit, content queue reconciliation, worktree snapshots, commit boundary planning, and slice handoff generation.',
        '- Documents the Windows Task Scheduler flow and manual review gates.',
      ]
    : [
        `- Prepares the ${group.title} slice.`,
        `- Keeps this change behind the ${group.gate} gate.`,
      ];

  const testPlan = verificationFor(group).map(command => `- [ ] \`${command}\``);
  return [
    `## Summary`,
    ...summary,
    '',
    '## Scope',
    `- Group: \`${group.id}\``,
    `- Gate: \`${group.gate}\``,
    `- Files: ${paths.length}`,
    '',
    '## Test Plan',
    ...testPlan,
    '',
    '## Deployment',
    group.gate === 'deploy-approval'
      ? '- Deployment requires explicit approval. This PR should not auto-deploy.'
      : '- No production deployment required for this slice.',
  ].join('\n');
}

function verificationFor(group) {
  if (group.id === 'loops_control_plane') {
    return [
      'node --check scripts/loops-24/run.mjs',
      'node --check scripts/loops-24/plan-commit-boundaries.mjs',
      'node --check scripts/loops-24/prepare-slice-handoff.mjs',
      'node scripts/loops-24/run.mjs',
      'node scripts/loops-24/plan-commit-boundaries.mjs',
      'node scripts/loops-24/prepare-slice-handoff.mjs --group loops_control_plane',
    ];
  }

  return [
    'git status --short',
    'node scripts/loops-24/plan-commit-boundaries.mjs',
    `node scripts/loops-24/prepare-slice-handoff.mjs --group ${group.id}`,
  ];
}

function parseStatusLine(line) {
  const status = line.slice(0, 2);
  return {
    status,
    path: line.slice(3).replaceAll('\\', '/'),
  };
}

function readArg(name) {
  const index = process.argv.indexOf(name);
  if (index === -1) return '';
  return process.argv[index + 1] || '';
}

async function readJson(file, fallback) {
  try {
    return JSON.parse(await fs.readFile(file, 'utf8'));
  } catch {
    return fallback;
  }
}

function runGit(args) {
  const result = spawnSync('git', args, { cwd: repoRoot, encoding: 'utf8', windowsHide: true });
  if (result.status !== 0) {
    throw new Error(result.stderr || `git ${args.join(' ')} failed`);
  }
  return result.stdout || '';
}

function escapePowerShellSingleQuoted(value) {
  return String(value).replaceAll("'", "''");
}

function hash(value) {
  return createHash('sha256').update(value).digest('hex').slice(0, 32);
}
